void ssd1306_update(int x0, int y0, int x1, int y1, uint8_t *buffer);
void ssd1306_set_orientation(uint8_t m);
void ssd1306_on();
void ssd1306_off();
void ssd1306_invert(uint8_t on);

#endif

#endif /* SSD1306_H_ */
